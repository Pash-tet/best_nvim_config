-- vim.opt — интерфейс к настройкам редактора (аналог :set в vimscript)
local opt = vim.opt

-- Номера строк
opt.number = true          -- показывать номер текущей строки
opt.relativenumber = true  -- остальные строки — относительные (удобно для 5j / 3dd)

-- Отступы
opt.tabstop = 2        -- сколько "пробелов" визуально занимает Tab
opt.shiftwidth = 2     -- отступ при >> / << / автоотступе
opt.expandtab = true   -- Tab на клавиатуре вставляет пробелы, а не символ \t
opt.autoindent = true  -- новая строка наследует отступ предыдущей

-- Поиск
opt.ignorecase = true  -- поиск без учёта регистра...
opt.smartcase = true   -- ...но если в запросе есть заглавная буква — учитывать регистр

-- Интерфейс
opt.termguicolors = true  -- 24-битный цвет (нужно для нормальных цветовых схем)
opt.foldlevel = 99  -- открывать файлы БЕЗ свёрнутых фолдов (иначе с treesitter-фолдингом всё схлопывается)
opt.signcolumn = "yes"    -- всегда резервировать колонку слева (под git-значки, ошибки LSP) — иначе текст "прыгает"
opt.scrolloff = 8         -- держать 8 строк отступа от курсора до края экрана при скролле
opt.laststatus = 3        -- ОДИН статуслайн (lualine) на весь редактор, а не свой на каждое окно/сплит
-- "~" в начале строк ПОСЛЕ конца файла — штатный индикатор Vim (эти строки
-- физически не существуют в буфере). eob=" " заменяет символ на пробел —
-- пустая область просто пустая, как в VSCode/JetBrains.
opt.fillchars = { eob = " " }
-- cmdheight=0 схлопывает НАТИВНУЮ командную строку (её высота обычно 1 строка
-- внизу, ПОД статуслайном). Без этого lualine не касается самого низа экрана —
-- под ним остаётся пустая зарезервированная полоса. Работает без потери
-- функциональности именно ПОТОМУ, что noice.nvim (lua/plugins/noice.lua)
-- перехватывает cmdline/messages и рисует их floating-окнами поверх текста —
-- native cmdline попросту больше не нужна для отображения. Без noice cmdheight=0
-- иногда "съедает" некоторые сообщения (E28 warning) — с noice это не проблема.
opt.cmdheight = 0

-- Файлы
opt.swapfile = false   -- не создавать .swp файлы
opt.undofile = true    -- сохранять историю undo между запусками nvim (файл на диске)

-- Разное
opt.mouse = "a"     -- мышь работает во всех режимах
opt.clipboard = "unnamedplus"  -- yank/paste используют системный буфер обмена
